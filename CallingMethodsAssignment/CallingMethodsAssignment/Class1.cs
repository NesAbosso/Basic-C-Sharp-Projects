using System;
using System.Collections.Generic;
using System.Text;

namespace CallingMethodsAssignment
{
    public class MathOperations
    {
        // This method multiplies the input by 2
        public int MultiplyByTwo(int input)
        {
            return input * 2;
        }

        // This method divides the input by 3
        public int DivideByThree(int input)
        {
            return input / 3;
        }

        // This method adds 5 to the input
        public int AddFive(int input)
        {
            return input + 5;
        }
    }
}
